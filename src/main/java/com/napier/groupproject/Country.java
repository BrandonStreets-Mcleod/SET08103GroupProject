package com.napier.groupproject;

/**
 * used to build country objects to allow for countries to be stored as singular objects that can be called
 */
public class Country {
    public String code;
    public String name;
    public String continent;
    public String region;
    public int area;
    public int indepYear;
    public int population;
    public int lifeExpec;
    public int GNP;
    public int GNPOld;
    public String localName;
    public String governmentForm;
    public String HeadOfState;
    public int capital;
    public int code2;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContinent() {
        return continent;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public int getIndepYear() {
        return indepYear;
    }

    public void setIndepYear(int indepYear) {
        this.indepYear = indepYear;
    }

    public int getPopulation() {
        return population;
    }

    public void setPopulation(int population) {
        this.population = population;
    }

    public int getLifeExpec() {
        return lifeExpec;
    }

    public void setLifeExpec(int lifeExpec) {
        this.lifeExpec = lifeExpec;
    }

    public int getGNP() {
        return GNP;
    }

    public void setGNP(int GNP) {
        this.GNP = GNP;
    }

    public int getGNPOld() {
        return GNPOld;
    }

    public void setGNPOld(int GNPOld) {
        this.GNPOld = GNPOld;
    }

    public String getLocalName() {
        return localName;
    }

    public void setLocalName(String localName) {
        this.localName = localName;
    }

    public String getGovernmentForm() {
        return governmentForm;
    }

    public void setGovernmentForm(String governmentForm) {
        this.governmentForm = governmentForm;
    }

    public String getHeadOfState() {
        return HeadOfState;
    }

    public void setHeadOfState(String headOfState) {
        HeadOfState = headOfState;
    }

    public int getCapital() {
        return capital;
    }

    public void setCapital(int capital) {
        this.capital = capital;
    }

    public int getCode2() {
        return code2;
    }

    public void setCode2(int code2) {
        this.code2 = code2;
    }
}


